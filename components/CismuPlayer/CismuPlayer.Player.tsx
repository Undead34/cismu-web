import { RootState } from "../../store/store";
import { connect } from "react-redux";
import { Grid } from "@mui/material";
import { Controls, Track, Options } from "../Controls/Controls";

interface Props {
  play: () => void;
  pause: () => void;
  stop: () => void;
  next: () => void;
  prev: () => void;
  onVolumeChange: (val: number) => number;
  onCurrentTimeChange: (val: number) => number;
  currentTime: number;
  duration: number;
  volume: number;
}

function CismuPlayerPlayer(props: Props) {
  const { play, pause, stop, next, prev } = props;
  const controlProps = { play, pause, stop, next, prev };

  return (
    <Grid
      container
      direction="row"
      bgcolor="#e1e1e1"
      height="75px"
      paddingX="20px"
      alignItems="center"
    >
      <Grid item>
        <Controls />
      </Grid>

      <Grid item flex="1">
        <Track />
      </Grid>

      <Grid item>
        <Options />
      </Grid>
    </Grid>
  );
}

function mapStateToProps(state: RootState) {
  const { state_player } = state;
  return {
    currentTime: state_player.currentTime,
    duration: state_player.duration,
    volume: state_player.volume,
  };
}

export default connect(mapStateToProps)(CismuPlayerPlayer);

